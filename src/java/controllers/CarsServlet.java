    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            CarService cs = new CarService();
            List<Car> cars = cs.getCars();
            
            HtmlComponents html = new HtmlComponents();
            out.println("<a href=\"/simpleLoginLogout\">Logout</a><br />");
            out.println(html.startHTML("Available Cars"));
            out.println("<table>");
            out.println(html.createTableHeadings("Brand", "Model", "Horsepower"));
            for(Car c : cars) {
                out.println(html.createTableData(c.getBrand(), c.getModel(), String.valueOf(c.getPower())));
            }
            out.println("</table>");
            out.println(html.endHTML());
        }
    }

